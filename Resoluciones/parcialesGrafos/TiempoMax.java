package parcialesGrafos;

public class TiempoMax {
		private int tiempoMax;
		private int recintosMax;
		
		public TiempoMax () {
			this.tiempoMax = 0;
			this.recintosMax = 0;
		}
		
		
		public int getTiempoMax() {
			return tiempoMax;
		}
		public void setTiempoMax(int tiempoMax) {
			this.tiempoMax = tiempoMax;
		}
		public int getRecintosMax() {
			return recintosMax;
		}
		public void setRecintosMax(int recintosMax) {
			this.recintosMax = recintosMax;
		}
}
